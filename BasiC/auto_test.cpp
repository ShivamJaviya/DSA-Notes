#include<bits/stdc++.h>
using namespace std;
int main() {
    auto i = 12;
    auto b = false;
    auto s = "Hi How are you?";
    auto v = {1, 2, 3}; 
    cout<<typeid(i).name()<<endl;
    cout<<typeid(b).name()<<endl;
    cout<<typeid(s).name()<<endl;
    cout<<typeid(v).name()<<endl;
    return 0;    
}
